import { mkdir, readdir, readFile, writeFile } from 'node:fs/promises';
import path from 'node:path';

const rootDir = process.cwd();
const sourceExtensions = new Set([
  '.cjs',
  '.cts',
  '.js',
  '.jsx',
  '.mjs',
  '.mts',
  '.ts',
  '.tsx',
]);
const ignoredDirNames = new Set([
  '.git',
  'build',
  'coverage',
  'dist',
  'node_modules',
]);
const scopeDefinitions = {
  all: {
    outputPath: path.join(rootDir, 'docs/generated/reusable-registry.md'),
    scanRoots: ['apps', 'packages'],
  },
  backend: {
    outputPath: path.join(
      rootDir,
      'docs/generated/reusable-registry.backend.md',
    ),
    scanRoots: ['apps/api/src'],
  },
  frontend: {
    outputPath: path.join(
      rootDir,
      'docs/generated/reusable-registry.frontend.md',
    ),
    scanRoots: ['apps/web/src'],
  },
  shared: {
    outputPath: path.join(
      rootDir,
      'docs/generated/reusable-registry.shared.md',
    ),
    scanRoots: ['packages/shared/src'],
  },
};

function parseScope(argv) {
  const scopeFlagIndex = argv.findIndex((value) => value === '--scope');

  if (scopeFlagIndex === -1) {
    return 'all';
  }

  const scopeName = argv[scopeFlagIndex + 1];

  if (!scopeName) {
    throw new Error(
      'Missing value for --scope. Use all, frontend, backend, or shared.',
    );
  }

  if (!Object.hasOwn(scopeDefinitions, scopeName)) {
    throw new Error(
      `Unsupported scope "${scopeName}". Use all, frontend, backend, or shared.`,
    );
  }

  return scopeName;
}

async function collectFiles(dir) {
  let entries;

  try {
    entries = await readdir(dir, { withFileTypes: true });
  } catch (error) {
    if (error.code === 'ENOENT') {
      return [];
    }

    throw error;
  }

  const files = [];

  for (const entry of entries) {
    if (ignoredDirNames.has(entry.name)) {
      continue;
    }

    const entryPath = path.join(dir, entry.name);

    if (entry.isDirectory()) {
      files.push(...(await collectFiles(entryPath)));
      continue;
    }

    if (entry.isFile() && sourceExtensions.has(path.extname(entry.name))) {
      files.push(entryPath);
    }
  }

  return files;
}

function getTag(block, tagName) {
  const match = block.match(new RegExp(`@${tagName}\\s+([^\\n\\r]+)`));

  return match?.[1].replace(/\s+\*\/$/, '').trim() ?? '';
}

function getLineNumber(source, index) {
  return source.slice(0, index).split('\n').length;
}

function getSymbol(afterBlock, blockEndIndex, source) {
  const match = afterBlock.match(
    /^\s*(?:export\s+)?(?:async\s+)?(?:function|class)\s+([A-Za-z_$][\w$]*)|^\s*(?:export\s+)?(?:const|let|var)\s+([A-Za-z_$][\w$]*)/m,
  );

  if (!match) {
    return null;
  }

  const name = match[1] ?? match[2];
  const index = blockEndIndex + match.index + match[0].indexOf(name);

  return {
    lineNumber: getLineNumber(source, index),
    name,
  };
}

function normalizeKeywords(keywords) {
  return [
    ...new Set(
      keywords
        .split(',')
        .map((keyword) => keyword.trim())
        .filter((keyword) => keyword && keyword !== 'reusable'),
    ),
  ].join(', ');
}

function extractReusableEntries(source, filePath) {
  const entries = [];
  const jsdocPattern = /\/\*\*[\s\S]*?\*\//g;
  let match;

  while ((match = jsdocPattern.exec(source)) !== null) {
    const block = match[0];

    if (!block.includes('@reusable')) {
      continue;
    }

    const symbol = getSymbol(
      source.slice(jsdocPattern.lastIndex),
      jsdocPattern.lastIndex,
      source,
    );

    if (!symbol) {
      continue;
    }

    entries.push({
      description: getTag(block, 'description'),
      filePath: path.relative(rootDir, filePath),
      keywords: normalizeKeywords(getTag(block, 'keywords')),
      lineNumber: symbol.lineNumber,
      symbolName: symbol.name,
    });
  }

  return entries;
}

function renderRegistry(entries, scopeName) {
  const lines = [
    '# Reusable Registry',
    '',
    `<!-- Generated by pnpm docs:scan-reusables --scope ${scopeName}. Do not edit manually. -->`,
    '',
  ];

  if (entries.length === 0) {
    lines.push('No reusable items found.', '');
    return lines.join('\n');
  }

  for (const entry of entries) {
    lines.push(`## ${entry.symbolName}`);
    lines.push('');
    lines.push(
      `- Description: ${entry.description || 'No description provided.'}`,
    );

    if (entry.keywords) {
      lines.push(`- Keywords: ${entry.keywords}`);
    }

    lines.push(`- File: ${entry.filePath}:${entry.lineNumber}`);
    lines.push('');
  }

  return lines.join('\n');
}

const scopeName = parseScope(process.argv.slice(2));
const { outputPath, scanRoots } = scopeDefinitions[scopeName];
const files = (
  await Promise.all(
    scanRoots.map((scanRoot) => collectFiles(path.join(rootDir, scanRoot))),
  )
).flat();
const entries = [];

for (const file of files) {
  const source = await readFile(file, 'utf8');
  entries.push(...extractReusableEntries(source, file));
}

entries.sort((left, right) => {
  const bySymbol = left.symbolName.localeCompare(right.symbolName);

  if (bySymbol !== 0) {
    return bySymbol;
  }

  return left.filePath.localeCompare(right.filePath);
});

await mkdir(path.dirname(outputPath), { recursive: true });
await writeFile(outputPath, renderRegistry(entries, scopeName));

console.log(
  `Generated ${path.relative(rootDir, outputPath)} with ${entries.length} reusable item(s).`,
);
